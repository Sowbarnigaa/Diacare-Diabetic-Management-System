<h2>What is TYPE-2 Diabetes?</h2>
<br>
<p ><img src="img5.jfif" alt="img5" style="float:right;width:600px;height:300px;">
Type 2 diabetes, the most common type of diabetes, is a disease that occurs when your blood glucose, also called blood sugar, is too high. Blood glucose is your main source of energy and comes mainly from the food you eat. Insulin, a hormone made by the pancreas, helps glucose get into your cells to be used for energy. In type 2 diabetes, your body doesn’t make enough insulin or doesn’t use insulin well. Too much glucose then stays in your blood, and not enough reaches your cells.Physical inactivity and certain health problems such as high blood pressure affect your chances of developing type 2 diabetes. You are also more likely to develop type 2 diabetes if you have prediabetes or had gestational diabetes when you were pregnant. Learn more about risk factors for type 2 diabetes.

</p>
<br><br>
<h2>TYPE-2 MEDICATION</h2><br>
<p><img src="img6.jfif" alt="img6" style="float:left;width:600px;height:350px;">
Symptoms of type 2 diabetes often develop slowly—over the course of several years—and can be so mild that you might not even notice them. Many people have no symptoms. Some people do not find out they have the disease until they have diabetes-related health problems, such as blurred vision or heart disease.Managing your blood glucose, blood pressure, and cholesterol, and quitting smoking if you smoke, are important ways to manage your type 2 diabetes. Lifestyle changes that include planning healthy meals, limiting calories if you are overweight, and being physically active are also part of managing your diabetes. So is taking any prescribed medicines. Work with your health care team to create a diabetes care plan that works for you.</p>



