<h2>Leafy greens</h2>
<br>
<p ><img src="img12.jpg" alt="img3" style="float:right;width:600px;height:250px;">
Green, leafy vegetables are low in calories, while also being extremely nutritious. Being low in digestible carbs means that they play a major role in controlling blood sugar levels too. Spinach, kale, and other leafy greens are rich in several vitamins and minerals like vitamin C. One study showed that increasing vitamin C intake reduced the fasting blood sugar levels for people with type 2 diabetes or high blood pressure levels.</p>
<br><br>
<h2>Cinnamon</h2><br>
<p><img src="img13.jfif" alt="img4" style="float:left;width:600px;height:250px;">
Along with its strong antioxidant properties, cinnamon is also known for its ability to control diabetes. According to several controlled studies, cinnamon can help reduce blood sugar levels and improve insulin sensitivity as well.

Care must be taken to limit your intake of cassia cinnamon to less than a single teaspoon a day. The presence of coumarin in this type of cinnamon has been linked to health problems at higher doses. On the other hand, Ceylon cinnamon does not contain much coumarin.</p>
<br><br>
<h2>Chiya seeds</h2><br>
<p><img src="img14.jpg" alt="img4" style="float:right;width:600px;height:250px;">
Chia seeds are extremely rich in fiber, yet contain less digestible carbs. The viscous fiber found in these seeds lowers blood sugar levels by slowing down the rate at which food is moving through the gut and getting absorbed.Additionally, the fiber present in chia seeds also helps one feel full, and in the process, prevents overeating and unnecessary weight gain. Chia seeds have also been shown to reduce blood pressure and inflammatory markers.</p><br><br><br>
