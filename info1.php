<p><img src="img4.jfif" alt="img4" style="float:left;width:600px;height:300px;">
Experts think type 1 diabetes is caused by genes and factors in the environment, such as viruses, that might trigger the disease.If you have type 1 diabetes, you must take insulin because your body no longer makes this hormone. Different types of insulin start to work at different speeds, and the effects of each last a different length of time. You may need to use more than one type. You can take insulin a number of ways. Common options include a needle and syringe, insulin pen, or insulin pump.</p>



