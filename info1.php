<h2>What is TYPE-1 Diabetes?</h2>
<br>
<p ><img src="img3.jpg" alt="img3" style="float:right;width:600px;height:300px;">
In most people with type 1 diabetes, the body’s immune system, which normally fights infection, attacks and destroys the cells in the pancreas that make insulin. As a result, your pancreas stops making insulin. Without insulin, glucose can’t get into your cells and your blood glucose rises above normal. People with type 1 diabetes need to take insulin every day to stay alive.Type 1 diabetes typically occurs in children and young adults, although it can appear at any age. Having a parent or sibling with the disease may increase your chance of developing type 1 diabetes. In the United States, about 5 percent of people with diabetes have type 1</p>
