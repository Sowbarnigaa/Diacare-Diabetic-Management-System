<h2>What is Pre-Diabetes?</h2>
<br>
<p ><img src="img7.jfif" alt="img7" style="float:right;width:600px;height:300px;">
Insulin resistance and prediabetes usually have no symptoms. Some people with prediabetes may have darkened skin in the armpit or on the back and sides of the neck, a condition called acanthosis nigricans. Many small skin growths called skin tags often appear in these same areas.

Even though blood glucose levels are not high enough to cause symptoms for most people, a few research studies have shown that some people with prediabetes may already have early changes in their eyes that can lead to retinopathy. This problem more often occurs in people with diabetes.</p>
<br><br>
<h2>Diagnosis</h2><br>
<p><img src="img8.png" alt="img8" style="float:left;width:600px;height:300px;">
The A1C test reflects your average blood glucose over the past 3 months. The FPG and OGTT show your blood glucose level at the time of the test. The A1C test is not as sensitive as the other tests. In some people, it may miss prediabetes that the OGTT could catch. The OGTT can identify how your body handles glucose after a mealâ€”often before your fasting blood glucose level becomes abnormal. Often doctors use the OGTT to check for gestational diabetes, a type of diabetes that develops during pregnancy.

People with prediabetes have up to a 50 percent chance of developing diabetes over the next 5 to 10 years. You can take steps to manage your prediabetes and prevent type 2 diabetes.</p>



